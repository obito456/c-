#include <iostream>
using namespace std;

int main() {
    long a=15;
    long int b=200;
    signed long c=-250;
    signed long int d=-300;
    unsigned long int e=350;

    cout<<"a size="<<sizeof(a)<<endl;
    cout<<"b size="<<sizeof(b)<<endl;
    cout<<"c size="<<sizeof(c)<<endl;
    cout<<"d size="<<sizeof(d)<<endl;
    cout<<"e size="<<sizeof(e)<<endl;

    return 0;
}
